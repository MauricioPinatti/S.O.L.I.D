package srp_principio_responsabilidade_unica;

public class DefinicaoSRP {

    /* SRP - Principio da Responsabilidade Única

       Em resumo, o princípio da responsabilidade única diz que: “Cada classe deve ter um, e somente um, motivo para mudar.”

       Se uma classe tem várias responsabilidades, mudar um requisito do projeto pode trazer várias razões para modificar a classe.

       Por isso, as classes devem ter responsabilidades únicas.

       Esse princípio pode ser estendido para os métodos que criamos também.

       Quanto mais tarefas um método executa, mais difícil é testá-lo e garantir que o programa está em ordem.

       Uma dica para aplicar o princípio na prática é tentar nomear suas classes ou métodos com tudo que eles são capazes de fazer.
     */
}
