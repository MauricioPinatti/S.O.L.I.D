package dip_principio_inversao_dependencia;

public class DefinicaoDIP {

    /*

            O Princípio da Inversão de Dependência diz: “dependa de abstrações e não de implementações concretas”.

            Assim, é recomendado que os módulos de alto nível não dependam diretamente dos detalhes de implementação de módulos de baixo nível.

            Em vez disso, eles devem depender de abstrações ou interfaces que definem contratos de funcionamento.

            Isso promove maior flexibilidade e facilita a manutenção do sistema.

            Implementação causa MUITO mais dependencia que uma abstração(interface - não foi implementado)

            injeção de dependência = é a forma como vc ajuda a resolver quando vc utiliza a inversão de dependência.

*/
}
